/**
* Write a program that displays the area and
* perimeter of a rectangle with the width of 4.5 and height of 7.9 using the following
* formula: area = width * height
*
* @author Ravi Lamontagne
*/

public class AreaPerimeterRectangle {
    public static void main (String [] args){
        System.out.println("Area of a rectangle with width of 4.5 and height of 7.9 is: " + 4.5 * 7.9);
        System.out.println("Perimeter of a rectangle with width of 4.5 and height of 7.9 is: " + ((4.5 * 2) + (7.9 * 2)));
    }
}
